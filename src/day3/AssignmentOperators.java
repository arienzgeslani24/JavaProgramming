package day3;

public class AssignmentOperators {

	public static void main(String[] args) {

		// Assignment = += -= *= /= %=

		// ex1
		int a = 10;
		a += 5; // a = a + 5;
		System.out.println(a); // 15

		int b = 10;
		b -= 5; // b = b - 5;
		System.out.println(b); // 5

		// ex2
		int c = 10;
		c *= 2; // c = c * 2;
		System.out.println(c); //20

		// ex3
		int d = 10;
		d /= 2; // d = d / 2;
		System.out.println(d); //5

		// ex4
		int e = 10;
		e %= 2; // e = e % 2;
		System.out.println(e); //0

	}

}
